package com.legstar.protobuf.cobol;

public class ProtoCobolException extends Exception {
	
	private static final long serialVersionUID = 1L;

	public ProtoCobolException() {
		super();
	}

	public ProtoCobolException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProtoCobolException(String message) {
		super(message);
	}

	public ProtoCobolException(Throwable cause) {
		super(cause);
	}




}
